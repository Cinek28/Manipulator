volatile uint16_t delayTimer;

void delay(uint16_t time){
	delayTimer= time;
	while(delayTimer!=0){
		;
	}
}

void TimingDelay_Decrement(void){
	if(delayTimer>0){
		delayTimer--;
	}
}

