		            	TIM_SetCompare3(TIM2,250);
		            	TIM_SetCompare4(TIM2,0);
		                break;
		            case 's':
		            	TIM_SetCompare3(TIM2,0);
		            	TIM_SetCompare4(TIM2,250);
		                break;
		            case 'e':
		            	GPIO_SetBits(GPIOD, GPIO_Pin_4);
		            	GPIO_ResetBits(GPIOD, GPIO_Pin_7);
		            	TIM_SetCompare2(TIM2,1000);
		                break;
